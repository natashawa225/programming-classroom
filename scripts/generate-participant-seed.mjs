import { createHash } from 'node:crypto'
import { mkdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const outputDir = join(process.cwd(), 'scripts', 'generated')
mkdirSync(outputDir, { recursive: true })

const csvLines = ['participant_id,group_name,plain_password']
const sqlRows = []
const usedPasswords = new Set()

function generateNumericPassword() {
  const length = Math.floor(Math.random() * 3) + 6 // 6, 7, or 8 digits
  let password = ''
  for (let i = 0; i < length; i += 1) {
    password += Math.floor(Math.random() * 10)
  }
  return password
}

function generateUniquePassword() {
  let pw
  do {
    pw = generateNumericPassword()
  } while (usedPasswords.has(pw))
  usedPasswords.add(pw)
  return pw
}

for (let index = 1; index <= 200; index += 1) {
  const participantId = `p${String(index).padStart(3, '0')}`
  const groupName = index <= 100 ? 'baseline' : 'treatment'
  const plainPassword = generateUniquePassword()
  const passwordHash = createHash('sha256').update(plainPassword).digest('hex')

  csvLines.push(`${participantId},${groupName},${plainPassword}`)
  sqlRows.push(
    `('${participantId}', '${groupName}', '${passwordHash}', 'sha256', TRUE)`
  )
}

const sql = [
  '-- Generated by scripts/generate-participant-seed.mjs',
  '-- Keep scripts/generated/participant-passwords.csv private.',
  'CREATE EXTENSION IF NOT EXISTS "pgcrypto";',
  '',
  'INSERT INTO public.participants (participant_id, group_name, password_hash, hash_algo, is_active)',
  'VALUES',
  sqlRows.map((row, index) => `${row}${index === sqlRows.length - 1 ? '' : ','}`).join('\n'),
  'ON CONFLICT (participant_id) DO UPDATE',
  'SET',
  '  group_name = EXCLUDED.group_name,',
  '  password_hash = EXCLUDED.password_hash,',
  '  hash_algo = EXCLUDED.hash_algo,',
  '  is_active = EXCLUDED.is_active;',
  '',
].join('\n')

writeFileSync(join(outputDir, 'participants_seed.sql'), sql)
writeFileSync(join(outputDir, 'participant-passwords.csv'), `${csvLines.join('\n')}\n`)

console.log('Generated participant seed files:')
console.log(`- ${join(outputDir, 'participants_seed.sql')}`)
console.log(`- ${join(outputDir, 'participant-passwords.csv')}`)
console.log(`Generated ${usedPasswords.size} unique passwords`)
